# 設計說明：
# 請使用選擇敘述撰寫一程式，根據使用者輸入的分數顯示對應的等級。標準如下表所示：
#
# 分　數	 等級
# 80 ~ 100  A
# 70 ~ 79   B
# 60 ~ 69	  C
# <= 59	  F
#
# 輸入輸出：
# 輸入說明
# 一個整數
#
# 輸出說明
# 判斷輸入值所對應的等級
#
# 範例輸入
# 79
# 範例輸出
# B

a = eval(input())
if 80 <= a <= 100:
    print("A")
elif 70 <= a <= 79:
    print("B")
elif 60 <= a <= 69:
    print("C")
else:
    print("F")