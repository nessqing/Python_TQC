# 設計說明：
# 請使用選擇敘述撰寫一程式，要求使用者輸入購物金額，購物金額需大於8,000（含）以上，並顯示折扣優惠後的實付金額。購物金額折扣方案如下表所示：
#
# 金　　額	折　扣
# 8,000（含）以上	9.5折
# 18,000（含）以上	9折
# 28,000（含）以上	8折
# 38,000（含）以上	7折
# 輸入輸出：
# 輸入說明
# 一個數值，需大於8,000（含）以上
#
# 輸出說明
# 顯示折扣優惠後的實付金額（輸出不需指定小數點位數）
#
# 範例輸入
# 12000
# 範例輸出
# 11400.0

a = eval(input())

if a >= 38000:
    print(f"{a*0.7}")
elif a >= 28000:
    print(f"{a*0.8}")
elif a >= 18000:
    print(f"{a * 0.9}")
elif a >= 8000:
    print(f"{a * 0.95}")