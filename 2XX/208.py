# 設計說明：
# 請使用選擇敘述撰寫一程式，讓使用者輸入一個十進位整數num(0 ≤ num ≤ 15)，將num轉換成十六進位值。
#
# 提示：轉換規則 = 十進位0~9的十六進位值為其本身，十進位10~15的十六進位值為A~F。
#
# 3. 輸入輸出：
# 輸入說明
# 一個數值
#
# 輸出說明
# 將此數值轉換成十六進位值
#
# 範例輸入1
# 13
# 範例輸出1
# D
#
# 範例輸入2
# 8
# 範例輸出2
# 8

a = input()
if a == "A":
    print(10)
elif a == "B":
    print(11)
elif a == "C":
    print(12)
elif a == "D":
    print(13)
elif a == "E":
    print(14)
elif a == "F":
    print(15)
else:
    print(a)